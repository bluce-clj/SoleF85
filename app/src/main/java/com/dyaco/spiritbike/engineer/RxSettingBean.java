package com.dyaco.spiritbike.engineer;

public class RxSettingBean {
    private int inclineMaxAd;
    private int inclineMinAd;
    private String status;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getInclineMaxAd() {
        return inclineMaxAd;
    }

    public void setInclineMaxAd(int inclineMaxAd) {
        this.inclineMaxAd = inclineMaxAd;
    }

    public int getInclineMinAd() {
        return inclineMinAd;
    }

    public void setInclineMinAd(int inclineMinAd) {
        this.inclineMinAd = inclineMinAd;
    }
}
